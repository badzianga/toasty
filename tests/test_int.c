#define TOASTY_IMPLEMENTATION
#include "toasty.h"

TEST(test_EqualInts) {
    TEST_ASSERT_EQUAL_INT(9998, 5000 + 4998);
}

TEST(test_EqualInt8s) {
    TEST_ASSERT_EQUAL_INT8(16, 8 + 8);
}

TEST(test_EqualInt16s) {
    TEST_ASSERT_EQUAL_INT16(32700, 32767 - 67);
}

TEST(test_EqualInt32s) {
    TEST_ASSERT_EQUAL_INT32(2147483600, 2147483647 - 47);
}

TEST(test_EqualInt64s) {
    TEST_ASSERT_EQUAL_INT64(9223372036854775800, 9223372036854775807 - 7);
}

TEST(test_EqualUnsignedInts) {
    TEST_ASSERT_EQUAL_UINT(20000, 5000 + 15000);
}

TEST(test_EqualUnsignedInt8s) {
    TEST_ASSERT_EQUAL_UINT8(140, 100 + 40);
}

TEST(test_EqualUnsignedInt16s) {
    TEST_ASSERT_EQUAL_UINT16(60000, 30000 + 30000);
}

TEST(test_EqualUnsignedInt32s) {
    TEST_ASSERT_EQUAL_UINT32(2147484000, 2147483999 + 1);
}

TEST(test_EqualUnsignedInt64s) {
    TEST_ASSERT_EQUAL_UINT64(18446744073709551615u, 18446744073709551614u + 1);
}

TEST(test_EqualHexs) {
    TEST_ASSERT_EQUAL_HEX(20000, 5000 + 15000);
}

TEST(test_EqualHex8s) {
    TEST_ASSERT_EQUAL_HEX8(140, 100 + 40);
}

TEST(test_EqualHex16s) {
    TEST_ASSERT_EQUAL_HEX16(60000, 30000 + 30000);
}

TEST(test_EqualHex32s) {
    TEST_ASSERT_EQUAL_HEX32(2147484000, 2147483999 + 1);
}

TEST(test_EqualHex64s) {
    TEST_ASSERT_EQUAL_HEX64(18446744073709551615llu, 18446744073709551614llu + 1llu);
}

int main() {
    return RunTests();
}
